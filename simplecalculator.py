firstnumber = int(input("first number... "))
operation = input("what is the operation?... ")
secondnumber = int(input("second number...  "))


if operation == "+":
    print("result is .. ", firstnumber + secondnumber)

elif operation == "-":
    print("result is .. ", firstnumber - secondnumber)

elif operation == "*":
    print("result is .. ", firstnumber * secondnumber)

elif operation == "/":
    print("result is .. ", firstnumber / secondnumber)

else:
    print("INVALID. SHUTING. DOWN. :( :( :( :( :(.             LOG OUT AND RESTART.:(        ")

